// sort Function
function sorted(){
   var selected=document.querySelector("#sor").value;
   var res=data1.sort(function(a,b){
	   if(selected=="Low"){
		   return a.price-b.price;
	   }
	   else if (selected=="High"){
		  return b.price-a.price;
	   }
   })
   mapping(res);
}

